    public void mos() throws SQLException{
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new Object[]{"CEDULA", "NOMBRE", "APELLIDO", "ESTADO", "CURSO", "FECHA"});
        ResultSet rs = sqlm.historialmatriculaAlumno();
        while(rs.next()){
            modelo.addRow(new Object[]{rs.getString("alu_cedula"), rs.getString("per_nombre"), rs.getString("per_apellido"), rs.getString("per_estado"), rs.getString("cur_nombre"), rs.getString("mat_fecha")});
        }
        jtablematriculas.setModel(modelo);
    }
    
