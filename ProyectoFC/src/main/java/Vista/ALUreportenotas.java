    private void jtfbuscarcursosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfbuscarcursosKeyReleased
        try {
            String asignatura = sqml.obtenerasignatura(cbasignatura.getSelectedItem().toString());
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Trabajo","Nota"});
            ResultSet rs = sqml.mReporteNotasAlumnoBuscar(asignatura, Login.alumno, jtfbuscarcursos.getText());
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("not_nombre"),rs.getString("not_nota")});
            }
            jTable1.setModel(modelo);
        } catch (SQLException ex) { System.out.println("mostrar datos de busqueda de reporte de notas alumno"); }
    }//GEN-LAST:event_jtfbuscarcursosKeyReleased

    private void jlbcargarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlbcargarMouseClicked
        try {
            String asignatura = sqml.obtenerasignatura(cbasignatura.getSelectedItem().toString());
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Trabajo","Nota"});
            ResultSet rs = sqml.mReporteNotasAlumno(asignatura, Login.alumno);
            while(rs.next()){
                modelo.addRow(new Object[]{rs.getString("not_nombre"),rs.getString("not_nota")});
            }
            jTable1.setModel(modelo);
        } catch (SQLException ex) { System.out.println("error mostrar reporte notas alumno"); }
    }//GEN-LAST:event_jlbcargarMouseClicked
     
    private void colocarImagen(JLabel lbl, String ruta){
        this.imagen = new ImageIcon(ruta);
        this.icono = new ImageIcon(
                this.imagen.getImage().getScaledInstance(
                        lbl.getWidth(), 
                        lbl.getHeight(), 
                        Image.SCALE_SMOOTH)
        );lbl.setIcon(this.icono);
        this.repaint();
    }

